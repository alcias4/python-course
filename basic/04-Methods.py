


animal = " camilo happy "


print(animal.upper()) # uppercase
print(animal.lower())  # lowercase
print(animal.capitalize()) # Capitalize
print(animal.title()) # first words transform a uppercase 
print(animal.strip()) # remove space of  left and right .
print(animal.strip().capitalize()) # concat of methods. 
print(animal.rstrip()) # remove space of left.
print(animal.lstrip())  # remove space of right.
print(animal.find("happy")) # search a character and return the index 
print(animal.replace("happy", "sad")) # replace a character for other .
print("sad" in animal) # return true o false. 
print( "sad" not in animal) # return no found character 
 